    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int id;

        if (jTextField1.getText().trim().equals("")) {
            id = -1;
        } else {
            try {
                id = Integer.parseInt(jTextField1.getText().trim());
                if (id <= 0) {
                    JOptionPane.showMessageDialog(this, "Enter a valid id!");
                    jTextField1.requestFocus();
                    return;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Enter a valid id!");
                jTextField1.requestFocus();
                return;
            }
        }

        String employeename = jTextField2.getText();

        int salary;
        if (jTextField3.getText().trim().equals("")) {
            salary = -1;
        } else {
            try {
                salary = Integer.parseInt(jTextField3.getText().trim());
                if (salary <= 0) {
                    JOptionPane.showMessageDialog(this, "Enter a valid Salary!");
                    jTextField3.requestFocus();
                    return;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Enter a valid Salary!");
                jTextField3.requestFocus();
                return;
            }
        }

        
        
        
        String birthDate = jTextField4.getText().trim();
        String employmentDate = jTextField5.getText().trim();
        String departmentName = jTextField6.getText().trim();

        String role=(String)jComboBox1.getSelectedItem();
       
        
        
        String salaryinfo="";
        if(jRadioButton1.isSelected())
            salaryinfo="less";
        else if(jRadioButton2.isSelected())
            salaryinfo="more";
        
        String birthinfo="";
        if(jRadioButton3.isSelected())
            birthinfo="before";
        else if(jRadioButton4.isSelected())
            birthinfo="after";
        
        
        String employmentinfo="";
        if(jRadioButton5.isSelected())
            employmentinfo="before";
        else if(jRadioButton6.isSelected())
            employmentinfo="after";
        
 
        
        try{
        JTable rep=  dbConnector.employeeReport(id, employeename, salary,salaryinfo , birthDate, birthinfo,employmentDate, employmentinfo, departmentName,role);
        General.viewTable(rep,100,100);
        }
        catch(Exception e){
            System.out.println(e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

