    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
         JTable temp;
        if ((id.getText().equals(""))){
            JOptionPane.showMessageDialog(this, "Please enter values in the search text fields");
            return;
        }
        else{
            try{
                int test=Integer.parseInt(id.getText().trim());
                }
            catch(Exception e){
                        JOptionPane.showMessageDialog(this, "Please enter a numeric ID");
                        return;
                }
            try{
            temp=dbConnector.searchRequest(id.getText());
            if(temp.getRowCount() <= 0){
            JOptionPane.showMessageDialog(this, "Error! Please enter details of an existing request to delete it");
            return;
            }
            }
            
            catch(Exception e){}
            
            }
        
            dbConnector.deleteRequest(id.getText());
            JOptionPane.showMessageDialog(this, "Request deletion succeded!");
    }//GEN-LAST:event_jButton5ActionPerformed

