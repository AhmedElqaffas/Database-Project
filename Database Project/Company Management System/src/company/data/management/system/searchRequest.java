    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
           if(newName.getText().equals("") && depTxt.getText().equals("") &&  supName.getText().equals("") && priceTxt.getText().equals("") && dateTxt.getText().equals("") && priority.getSelectedItem().toString().equals("--")){
           JOptionPane.showMessageDialog(this, "Please enter values in the textfields");
           return;
      }
      if(!(priceTxt.getText().equals(""))){
      try{
         int x=Integer.parseInt(priceTxt.getText().trim());
      }
      catch(Exception e){
          JOptionPane.showMessageDialog(this, "Please enter a valid numeric price value");
          return;
      }
      }
      if(!dateTxt.getText().equals("") && dateTxt.getText().length()!=10){
          JOptionPane.showMessageDialog(this, "Error! please enter the purchase date in the correct form");
          return;
      }
        try {
            dbConnector.modifyRequest(id.getText().trim(),newName.getText().trim(), priority.getSelectedItem().toString(), depTxt.getText().trim()
                    , yes.isSelected(), supName.getText().trim(), priceTxt.getText().trim(), dateTxt.getText().trim());
            JOptionPane.showMessageDialog(this, "Request has been modified successfully!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton3ActionPerformed

